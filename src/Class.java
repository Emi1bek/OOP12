public class Class {
    private int number;
    private String string;
    private int[] array;

    public Class(int number, String string, int[] array) {
        this.number = number;
        this.string = string;
        this.array = array;
    }

    public Class() {
    }


    public int getNumber() {
        return number;
    }

    public String getString() {
        return string;
    }

    public int[] getArray() {
        return array;
    }
}
